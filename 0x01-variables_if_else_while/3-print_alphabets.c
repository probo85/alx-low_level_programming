#include <stdio.h>
#include <stdlib.h>

/**
 *main-print both the uppercase and the lowercase alphabets
 *Return: using putchar
 */
int main(void)
{
	char ch = 'a';

	for (ch = 'a'; ch <= 'z'; ch++)
	{
		putchar(ch);
	}
	for (ch = 'A'; ch <= 'Z'; ch++)
	{
		putchar(ch);
	}
	putchar('\n');
	return (0);
}
